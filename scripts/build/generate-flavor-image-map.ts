/**
 * Build-time helper: scan public/images/flavors/ and emit a static
 * `data/flavorImagesGenerated.ts` mapping numeric flavor id → public URL.
 *
 * Why: doing the fs.readdirSync at runtime caused Next.js' file tracer to
 * bundle every image under public/images/flavors/ into the serverless
 * function output, which blew past Vercel's 250MB unzipped function size
 * limit. By baking the map at build time we ship just a JSON object in
 * the function bundle and let the static asset pipeline serve the actual
 * images from the CDN.
 *
 * Usage (run automatically by `pnpm build` via the prebuild hook):
 *   npx tsx scripts/build/generate-flavor-image-map.ts
 */
import fs from 'node:fs'
import path from 'node:path'

const FLAVOR_IMAGES_DIR = path.join(process.cwd(), 'public', 'images', 'flavors')
const OUT = path.join(process.cwd(), 'data', 'flavorImagesGenerated.ts')
const SUPPORTED_EXTENSIONS = new Set(['.png', '.jpg', '.jpeg', '.webp', '.avif'])

function build() {
  const entries: Record<number, string> = {}
  if (fs.existsSync(FLAVOR_IMAGES_DIR)) {
    for (const file of fs.readdirSync(FLAVOR_IMAGES_DIR)) {
      const ext = path.extname(file).toLowerCase()
      if (!SUPPORTED_EXTENSIONS.has(ext)) continue
      const stem = path.basename(file, ext)
      const id = Number(stem)
      if (!Number.isFinite(id)) continue
      entries[id] = `/images/flavors/${file}`
    }
  }
  const sortedKeys = Object.keys(entries).map(Number).sort((a, b) => a - b)
  const sorted: Record<number, string> = {}
  for (const k of sortedKeys) sorted[k] = entries[k]

  const banner = '// AUTO-GENERATED by scripts/build/generate-flavor-image-map.ts. Do not edit.\n'
  const body = `export const FLAVOR_IMAGE_MAP: Record<number, string> = ${JSON.stringify(sorted, null, 2)}\n`
  fs.writeFileSync(OUT, banner + body)
  process.stderr.write(`flavor image map: ${sortedKeys.length} entries → ${OUT}\n`)
}

build()
